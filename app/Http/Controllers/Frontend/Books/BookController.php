     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(BookRepository $bookRepo, CategoryRepository $categoryRepo, $category_id = null)
    {
        $categories = app('categories');
        $cart = app('getCartByCustomerId');
        $cartCount = app('cartCount');
        $wishlistCount = app('wishlistCount');
        $total = app('total');
        if($category_id){
            $books = $categoryRepo->getCategory($category_id)->books;
        }else{
            $books = $bookRepo->getBooks(20);
        }
        return response()->view('frontend.books.index', [
            'categories' => $categories, 
            'books' => $books, 
            'cart' => $cart,
            'total' => $total,
            'cartCount' => $cartCount,
            'wishlistCount' => $wishlistCount,
        ]);
    }

